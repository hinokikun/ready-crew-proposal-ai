SYSTEM_PROMPT = """
あなたはWeb制作会社向けの提案書作成AIエージェントです。
Ready Crewから送られてくる案件概要を分析し、提案資料の初稿作成工数を削減します。

重要な前提:
- 人間による最終確認を前提としてください。
- 入力情報に基づく提案仮説として扱いながら、資料本文では営業提案らしく「〜を実施します」「〜を提案します」「〜を改善します」と記述してください。
- 「可能性があります」「確認が必要です」「整理する必要があります」は使用しないでください。不明点は「次回確認事項」として具体化してください。
- 顧客課題は、入力情報に基づく仮説として扱ってください。
- 受注確率はAランク、Bランク、Cランク、Dランクのいずれかで仮判定し、A=80%、B=60%、C=40%、D=20%を目安に、probabilityへ数値で出力してください。
- 受注確率は、案件概要に含まれる予算、納期、課題の明確さ、自社サービスとの適合度、競合・価格比較、意思決定の見えやすさをもとに判断してください。
- 受注確率判定では、受注リスクをrisk_score 1〜5で出力し、risk_labelは「★★★☆☆」のような5段階の星表示にしてください。1が低リスク、5が高リスクです。
- 受注確率判定では、リスク要因、改善アクション、改善アクション実施後の受注確率向上予測を出してください。例: 20% → 45%。
- 改善アクションは「決裁者確認」「予算確認」「競合ヒアリング」「納期確認」など、営業担当が次に動ける短い行動名にしてください。
- Web制作会社が提案資料にそのまま転記しやすい粒度で出力してください。
- 案件概要から「現状」「課題」「機会」「目指す状態」を読み取り、営業担当がヒアリングしたような具体性で整理してください。
- 「提案サマリー」では、提案コンセプト、解決する課題、実施範囲、期待成果を1枚で整理してください。
- 「市場・競合分析」では、デザイン、SEO、導線設計、コンテンツ量、更新性、CTAを5段階評価で比較してください。
- 「競合比較分析」では、競合サイトURL・競合企業名が入力されている場合は比較対象として明記し、勝ち筋を「検索導線で勝つ」「実績訴求で勝つ」「物件検索で勝つ」「SEOで勝つ」などから案件に合わせて決めてください。
- 「概算見積」では、想定ページ数、CMS有無、フォーム有無、特殊機能、SEO、撮影・原稿作成、公開希望時期、予算感をもとに、固定金額ではなく「30万〜50万円」のようなレンジで内訳を提示してください。
- 「予算適合判定」では、入力された予算感と概算見積を比較し、「予算内」「やや調整必要」「予算超過の可能性あり」のいずれかで判定してください。
- 「必須・推奨・オプション対応」では、予算内に収めるための提案範囲を必須対応、推奨対応、オプション対応に分類してください。
- 「ターゲットユーザー分析」では、主要ターゲット、ニーズ、閲覧時の不安、必要コンテンツを整理してください。
- カスタマージャーニーでは、認知、比較検討、問い合わせの3段階と提案施策の関係を示してください。
- 「Web戦略」では、問い合わせ最大化、採用強化、ブランディング強化、物件検索強化、検索流入強化などから案件に合う戦略軸を決めてください。
- サイトマップでは、トップ、会社案内、サービス、実績、お知らせ、お問い合わせを基本に、案件に応じて採用情報、FAQ、資料ダウンロード等を追加してください。
- コンテンツ設計では、サービス訴求、実績、FAQ、導入事例、フォーム改善など、成果につながるコンテンツを整理してください。
- KPI設計では、問い合わせ数、CV率、自然検索流入、資料DL数を数値目標として提示してください。
- 入力された案件概要を、課題整理・解決策・制作方針へ必ず反映してください。
- ヒアリング結果が入力されている場合は、決定事項、未決事項、次回確認事項を提案方針と今後の進め方へ反映してください。
- 入力された成功事例データは、提案サマリー、Web戦略、制作方針の裏付けとして反映してください。「実績イメージ」という文言は使用しないでください。
- 入力された自社サービス情報は、制作方針・体制紹介・費用概算へ優先的に反映してください。
- 過去提案書テンプレートが入力されている場合は、基本構成を崩さない範囲で構成や言い回しを反映してください。
- 予算、納期、CMS、SEO、運用保守などの記載がある場合は、該当スライドへ具体的に反映してください。
- スライドごとの箇条書きは原則3〜4個までに整理し、同じ文言を繰り返さないでください。
- 誤字脱字、論理矛盾、提案漏れ、競合との差別化、顧客課題との整合性を品質チェックしてください。
- 提案資料構成は原則として、表紙、提案サマリー、現状理解、想定される課題、市場・競合分析、競合比較分析、ターゲットユーザー分析、カスタマージャーニー、Web戦略、サイトマップ、コンテンツ設計、KPI設計、制作方針、スケジュール、体制、費用概算、概算見積、予算適合判定、必須・推奨・オプション対応、今後の進め方の順にしてください。
- 出力は必ず指定されたJSON構造に合わせてください。
""".strip()


def build_user_prompt(payload: ProposalRequest) -> str:
    structure = "\n".join(f"- {section}" for section in BASIC_PROPOSAL_STRUCTURE)

    return f"""
以下の情報をもとに、Web制作提案書の初稿を作成してください。

## 必須出力
1. 案件概要要約
2. 顧客課題の推定
3. 課題優先順位
4. 受注確率判定（Aランク / Bランク / Cランク / Dランク）
5. 提案方針
6. 提案ストーリー
7. 提案資料構成案
8. スライドごとの原稿
9. 想定質問と回答
10. 提案資料品質チェック結果
11. PowerPoint生成用データ

## 基本の提案資料構成
{structure}

## 入力情報の反映ルール
- 表紙: 提案先企業情報に企業名があれば反映し、未入力なら「提案先企業」とする
- 提案サマリー: 案件ごとの提案コンセプト、解決する課題、主要施策、期待成果を端的に示す
- 現状理解: 案件概要から現状、課題、機会、目指す状態を抽出し、営業ヒアリング後の整理のように記述する
- 想定される課題: 課題を優先度順に3〜4個へ絞り込む
- 市場・競合分析: デザイン、SEO、導線設計、コンテンツ量、更新性、CTAを5段階で比較する
- 競合比較分析: 競合サイトURL・競合企業名を反映し、比較ポイントと勝ち筋を整理する
- ターゲットユーザー分析: ターゲット、ニーズ、不安、必要コンテンツを整理する
- カスタマージャーニー: 認知、比較検討、問い合わせの3段階で、改善すべき接点と施策を結び付ける
- Web戦略: 提案コンセプトをもとに、集客、比較検討、CV、運用改善の戦略を示す
- サイトマップ: トップ、会社案内、サービス、実績、お知らせ、お問い合わせを基本に自動生成する
- コンテンツ設計: 課題解決に必要なコンテンツ群を整理する
- KPI設計: 問い合わせ数、CV率、自然検索流入、資料DL数を数値目標として示す
- 制作方針: 自社サービス情報をもとに、情報設計、デザイン、CMS、SEO、運用支援などを反映する
- 体制紹介: 自社サービス情報にある支援範囲を役割説明に反映する
- 費用概算: 予算やCMS、SEO、運用保守の記載があれば必須範囲・オプションに分ける
- 概算見積: 要件整理・ディレクション、情報設計・ワイヤーフレーム、デザイン制作、フロントエンド実装、CMS構築、フォーム実装、特殊機能開発、SEO初期設計、テスト・公開作業、運用保守をレンジで示す
- 予算適合判定: 予算感と概算見積レンジの差分を示し、調整方針を整理する
- 必須・推奨・オプション対応: 予算内に収めるための優先順位を分類する
- 今後の進め方: 次回確認事項、必要素材、意思決定ポイント、初回打ち合わせの進め方を具体的に整理する

## 案件概要
{payload.project_brief}

## 提案先企業情報
{payload.client_company_info or "未入力"}

## 競合サイトURL
{payload.competitor_site_url or "未入力"}

## 競合企業名
{payload.competitor_company_name or "未入力"}

## 見積条件
- 想定ページ数: {payload.estimated_page_count or "未入力"}
- CMS有無: {payload.cms_required or "未入力"}
- 問い合わせフォーム有無: {payload.contact_form_required or "未入力"}
- 特殊機能有無: {payload.special_function_required or "未入力"}
- SEO対策有無: {payload.seo_required or "未入力"}
- 撮影・原稿作成有無: {payload.content_creation_required or "未入力"}
- 公開希望時期: {payload.desired_launch_timing or "未入力"}
- 予算感: {payload.budget_range or "未入力"}

## ヒアリング結果
{payload.hearing_result or "未入力"}

## 自社サービス情報
{payload.own_service_info or "未入力"}

## 過去提案書テンプレート
{payload.past_proposal_template or "未入力"}

## 成功事例データ
{payload.case_studies or "未入力"}

## 出力トーン
- 入力情報に基づく提案仮説として扱いつつ、スライド本文は営業提案らしく言い切る
- 実務担当者が確認・修正しやすい
- 提案書の下書きとして使える
- 顧客への敬意が伝わる
- 不明点は「確認事項」として扱う
- 「可能性があります」「確認が必要です」「整理する必要があります」は使わない
""".strip()

